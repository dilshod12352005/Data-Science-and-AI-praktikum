import torch
import torch.nn as nn
from torch.utils.data import TensorDataset, DataLoader

# 1. Ma'lumotlarni PyTorch tensorlariga o'giramiz
# Har bir encoded array bir xil max_len (256) o'lchamda bo'lishi shart
X_np = np.array(df_final['encoded_reviews'].tolist(), dtype=np.int64)
y_np = np.array(df_final['label'].tolist(), dtype=np.float32)

X_tensor = torch.tensor(X_np)
y_tensor = torch.tensor(y_np)

# DataLoader tayyorlaymiz (Batch size: 32)
dataset = TensorDataset(X_tensor, y_tensor)
train_loader = DataLoader(dataset, batch_size=32, shuffle=True)

# 2. SentimentRNN Klassini yaratish
class SentimentRNN(nn.Module):
    def __init__(self, vocab_size, embedding_dim, hidden_dim):
        super(SentimentRNN, self).__init__()
        self.embedding = nn.Embedding(vocab_size, embedding_dim, padding_idx=0)
        self.rnn = nn.RNN(embedding_dim, hidden_dim, batch_first=True)
        self.fc = nn.Linear(hidden_dim, 1)
        self.sigmoid = nn.Sigmoid()

    def forward(self, x):
        # x shakli: [batch_size, seq_len]
        out = self.embedding(x)  # [batch_size, seq_len, embedding_dim]
        out, h_n = self.rnn(out) # out: [batch_size, seq_len, hidden_dim]

        # Eng oxirgi vaqt qadamining (last time step) chiqishini olamiz
        last_hidden = out[:, -1, :] # [batch_size, hidden_dim]

        out = self.fc(last_hidden)  # [batch_size, 1]
        out = self.sigmoid(out)     # [batch_size, 1]
        return out.squeeze()        # [batch_size]

# 3. Model, Loss va Optimizatorni sozlash
vocab_size = 4002 # 4000 ta so'z + <pad> + <unk>
embedding_dim = 128
hidden_dim = 256

device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
rnn_model = SentimentRNN(vocab_size, embedding_dim, hidden_dim).to(device)

criterion = nn.BCELoss()
optimizer = torch.optim.Adam(rnn_model.parameters(), lr=0.001)

# 4. O'qitish sikli (10 Epoxa)
print(f"Model {device} qurilmasida o'qitilmoqda...")
rnn_model.train()

for epoch in range(10):
    total_loss = 0.0
    for batch_x, batch_y in train_loader:
        batch_x, batch_y = batch_x.to(device), batch_y.to(device)

        optimizer.zero_grad()
        predictions = rnn_model(batch_x)

        loss = criterion(predictions, batch_y)
        loss.backward()
        optimizer.step()

        total_loss += loss.item()

    avg_loss = total_loss / len(train_loader)
    print(f"Epoch [{epoch+1}/10], Loss: {avg_loss:.4f}")

