# 1. Modelni qayta yuklaymiz
model = AutoModelForSequenceClassification.from_pretrained("distilbert-base-uncased", num_labels=2)

# 2. TrainingArguments ichidagi 'evaluation_strategy' ni 'eval_strategy' ga almashtirdik!
training_args = TrainingArguments(
    output_dir="./results",
    num_train_epochs=3,              # Shart bo'yicha 3 epoxa
    per_device_train_batch_size=16,
    per_device_eval_batch_size=16,
    logging_steps=100,
    eval_strategy="epoch",           # Mana shu yer to'g'rilandi (eval_strategy)
    save_strategy="epoch",
    learning_rate=2e-5,
    weight_decay=0.01,
    report_to="none"
)

# 3. Trainer obyektini yaratish
trainer = Trainer(
    model=model,
    args=training_args,
    train_dataset=tokenized_train,
    eval_dataset=tokenized_test,
)

# 4. Modelni o'qitish
