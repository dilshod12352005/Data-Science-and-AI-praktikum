# 6.1. Bashorat funksiyalarini yaratish

# A) SentimentRNN uchun bashorat funksiyasi
def predict_rnn(text):
    rnn_model.eval()
    with torch.no_grad():
        # Matnni kodlaymiz
        encoded = encode_review(text, vocab)
        tensor_input = torch.tensor([encoded]).to(device) # Batch o'lchami 1 qilinadi

        # Model prediction
        output = rnn_model(tensor_input)
        # Agar output bitta skalyar bo'lsa, uni element sifatida olamiz
        prob = output.item() if tensor_input.size(0) == 1 and len(output.shape) == 0 else output[0].item()

        return "positive" if prob >= 0.5 else "negative"

# B) Transformer uchun bashorat funksiyasi
# pipeline orqali matnni to'g'ridan-to'g'ri berish juda qulay
classifier_pipeline = pipeline("text-classification", model=model, tokenizer=tokenizer, device=0 if torch.cuda.is_available() else -1)

def predict_transformer(text):
    # max_length dan oshib ketmasligi uchun matnni qisqartirib uzatamiz
    result = classifier_pipeline(text, truncation=True, max_length=256)[0]
    # Model logitlariga qarab label0 -> negative, label1 -> positive yoki model konfiguratsiyasiga ko'ra moslashadi
    if result['label'] == 'LABEL_1':
        return "positive"
    elif result['label'] == 'LABEL_0':
        return "negative"
    else:
        return result['label'].lower()

