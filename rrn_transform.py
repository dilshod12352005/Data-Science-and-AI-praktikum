import collections
import re

# So'zlarni tozalash va tokenizatsiya qilish funksiyasi
def tokenize(text):
    # Hamma harflarni kichik qilamiz va belgilardan tozalaymiz
    text = text.lower()
    text = re.sub(re.compile('<.*?>'), '', text) # <br /> teglarni tozalash
    words = re.findall(r'\b\w+\b', text)
    return words

# 1. Barcha sharhlardagi so'zlarni to'playmiz va chastotasini hisoblaymiz
all_words = []
for review in df_final['Reviews']:
    all_words.extend(tokenize(review))

word_counts = collections.Counter(all_words)

# 2. Eng ko'p uchraydigan 4000 ta so'zni ajratib olamiz
most_common_words = [word for word, count in word_counts.most_common(4000)]

# 3. Lug'at (vocab) yaratamiz va maxsus tokenlarni qo'shamiz
vocab = {'<pad>': 0, '<unk>': 1}
for idx, word in enumerate(most_common_words):
    vocab[word] = idx + 2  # 0 va 1 band bo'lgani uchun 2 dan boshlaymiz

print(f"Lug'at o'lchami: {len(vocab)}")

# 4. Matnni sonli ketma-ketlikka o'giruvchi funksiya
def encode_review(text, vocab, max_len=256):
    words = tokenize(text)
    # Agar so'z lug'atda bo'lsa indeksini, bo'lmasa <unk> (1) indeksini qo'yamiz
    encoded = [vocab.get(word, 1) for word in words]

    # Shart bo'yicha uzunlikni chegaralash (truncation) yoki to'ldirish (padding)
    if len(encoded) < max_len:
        encoded += [0] * (max_len - len(encoded)) # <pad> bilan to'ldirish
    else:
        encoded = encoded[:max_len] # qirqib tashlash

    return encoded

# Yangi ustun sifatida DataFrame'ga qo'shamiz
df_final['encoded_reviews'] = df_final['Reviews'].apply(lambda x: encode_review(x, vocab))

# Toifani ham sonlarga o'giramiz: pos -> 1, neg -> 0
df_final['label'] = df_final['Sentiment'].apply(lambda x: 1 if x == 'pos' else 0)

# Tekshirish
