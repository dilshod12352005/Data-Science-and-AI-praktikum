# 4.2. distilbert-base-uncased tokenizer'ini yuklash
tokenizer = AutoTokenizer.from_pretrained("distilbert-base-uncased")

def preprocess_function(examples):
    # Matnlarni tokenizatsiya qilamiz, padding va truncation qo'llaymiz (max_length=256)
    return tokenizer(examples['text'], padding="max_length", truncation=True, max_length=256)

# Funksiyani train va test to'plamlariga map metodi orqali qo'llaymiz
tokenized_train = train_dataset.map(preprocess_function, batched=True)
tokenized_test = test_dataset.map(preprocess_function, batched=True)

# Tekshirish uchun ustunlar nomini ko'ramiz
print("\nTokenizatsiyadan keyingi ustunlar:", tokenized_train.column_names)

from transformers import AutoModelForSequenceClassification, TrainingArguments, Trainer, pipeline
import numpy as np
import torch

# =====================================================================
# 5. TRANSFORMER MODELINI O'QITISH VA BAHOLASH (To'g'rilangan versiya)
